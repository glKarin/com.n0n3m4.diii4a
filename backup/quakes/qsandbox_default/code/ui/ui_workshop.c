static sfxHandle_t Workshop_MenuKey( int key ) {
	if( key == K_MOUSE2 || key == K_ESCAPE ) {
		//UI_PopMenu();
		UI_LoadArenas();
		//trap_Cmd_ExecuteText( EXEC_APPEND, "game_restart");
	}
	if( key == K_F5 ) {
	UI_Workshop_LoadItemsFromFile("workshop_items.cfg");	
	}
	return Menu_DefaultKey( &s_workshop.menu, key );
}


