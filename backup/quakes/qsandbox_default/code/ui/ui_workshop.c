/*
===============
UI_Workshop_MenuEvent
===============
*/
static void UI_Workshop_MenuEvent( void *ptr, int event ) {
	fileHandle_t 	handle;
	int 			len;

	if( event != QM_ACTIVATED ) {
		return;
	}

	switch ( ((menucommon_s*)ptr)->id ) {
	case ID_GO:
		len = trap_FS_FOpenFile(va("%s.pk3", s_workshop.list.itemnames[s_workshop.list.curvalue]), &handle, FS_READ);
		trap_Cmd_ExecuteText( EXEC_APPEND, "toggleconsole \n" );
		if (len < 0)
		trap_Cmd_ExecuteText( EXEC_APPEND, va("download %s \n", s_workshop.list.itemnames[s_workshop.list.curvalue]) );
		else
		trap_Cmd_ExecuteText( EXEC_APPEND, va("echo Map already downloaded: %s \n", s_workshop.list.itemnames[s_workshop.list.curvalue]) );
		trap_FS_FCloseFile(handle);
		break;
		
	case ID_LIST:
		len = trap_FS_FOpenFile(va("%s.pk3", s_workshop.list.itemnames[s_workshop.list.curvalue]), &handle, FS_READ);
		trap_Cmd_ExecuteText( EXEC_APPEND, "toggleconsole \n" );
		if (len < 0)
		trap_Cmd_ExecuteText( EXEC_APPEND, va("download %s \n", s_workshop.list.itemnames[s_workshop.list.curvalue]) );
		else
		trap_Cmd_ExecuteText( EXEC_APPEND, va("echo Map already downloaded: %s \n", s_workshop.list.itemnames[s_workshop.list.curvalue]) );
		trap_FS_FCloseFile(handle);
		break;

	case ID_BACK:
		UI_PopMenu();
		UI_LoadArenas();
		//trap_Cmd_ExecuteText( EXEC_APPEND, "game_restart");
		break;
	}
}


