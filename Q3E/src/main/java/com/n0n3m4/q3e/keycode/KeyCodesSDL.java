    private static int keycode_to_SDL(int keycode)
    {
        /* FIXME: If this function gets too unwieldy in the future, replace with a lookup table */
        int button = 0;
        switch (keycode) {
            /* Some gamepad buttons (API 9) */
            case KeyEvent.KEYCODE_BUTTON_A:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_A;
                break;
             case KeyEvent.KEYCODE_BUTTON_B:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_B;
                break;
             case KeyEvent.KEYCODE_BUTTON_X:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_X;
                break;
             case KeyEvent.KEYCODE_BUTTON_Y:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_Y;
                break;
             case KeyEvent.KEYCODE_BUTTON_L1:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_LEFTSHOULDER;
                break;
             case KeyEvent.KEYCODE_BUTTON_R1:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_RIGHTSHOULDER;
                break;
             case KeyEvent.KEYCODE_BUTTON_THUMBL:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_LEFTSTICK;
                break;
             case KeyEvent.KEYCODE_BUTTON_THUMBR:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_RIGHTSTICK;
                break;
             case KeyEvent.KEYCODE_MENU:
             case KeyEvent.KEYCODE_BUTTON_START:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_START;
                break;
             case KeyEvent.KEYCODE_BACK:
             case KeyEvent.KEYCODE_BUTTON_SELECT:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_BACK;
                break;
             case KeyEvent.KEYCODE_BUTTON_MODE:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_GUIDE;
                break;
             case KeyEvent.KEYCODE_BUTTON_L2:
                button = 15;
                break;
             case KeyEvent.KEYCODE_BUTTON_R2:
                button = 16;
                break;
             case KeyEvent.KEYCODE_BUTTON_C:
                button = 17;
                break;
             case KeyEvent.KEYCODE_BUTTON_Z:
                button = 18;
                break;

            /* D-Pad key codes (API 1) */
             case KeyEvent.KEYCODE_DPAD_UP:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_DPAD_UP;
                break;
             case KeyEvent.KEYCODE_DPAD_DOWN:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_DPAD_DOWN;
                break;
             case KeyEvent.KEYCODE_DPAD_LEFT:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_DPAD_LEFT;
                break;
             case KeyEvent.KEYCODE_DPAD_RIGHT:
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_DPAD_RIGHT;
                break;
             case KeyEvent.KEYCODE_DPAD_CENTER:
                /* This is handled better by applications as the A button */
                /*button = 19;*/
                button = SDL_GameControllerButton.SDL_CONTROLLER_BUTTON_A;
                break;

            /* More gamepad buttons (API 12), these get mapped to 20...35*/
             case KeyEvent.KEYCODE_BUTTON_1:
             case KeyEvent.KEYCODE_BUTTON_2:
             case KeyEvent.KEYCODE_BUTTON_3:
             case KeyEvent.KEYCODE_BUTTON_4:
             case KeyEvent.KEYCODE_BUTTON_5:
             case KeyEvent.KEYCODE_BUTTON_6:
             case KeyEvent.KEYCODE_BUTTON_7:
             case KeyEvent.KEYCODE_BUTTON_8:
             case KeyEvent.KEYCODE_BUTTON_9:
             case KeyEvent.KEYCODE_BUTTON_10:
             case KeyEvent.KEYCODE_BUTTON_11:
             case KeyEvent.KEYCODE_BUTTON_12:
             case KeyEvent.KEYCODE_BUTTON_13:
             case KeyEvent.KEYCODE_BUTTON_14:
             case KeyEvent.KEYCODE_BUTTON_15:
             case KeyEvent.KEYCODE_BUTTON_16:
                button = 20 + (keycode - KeyEvent.KEYCODE_BUTTON_1);
                break;

            default:
                return -1;
            /* break; -Wunreachable-code-break */
        }

        /* This is here in case future generations, probably with six fingers per hand,
         * happily add new cases up above and forget to update the max number of buttons.
         */
        final int ANDROID_MAX_NBUTTONS = 36;
        //SDL_assert(button < ANDROID_MAX_NBUTTONS);
        return button;
    }

